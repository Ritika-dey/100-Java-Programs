class EnhanceLoop{
    public static void main(String[] args){ 
        int num[] = {1,2,3,4,5};
        for(int i: num){
            System.out.println(i);
        }

        char s[] = {'s','t','r','i','n','g'};
        for(char x: s){
            System.out.print(x+" ");
        }
        System.out.println();
    }
}